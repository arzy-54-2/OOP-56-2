class Rectangle:
    def __init__(self, width, height):
        self._width = width
        self._height = height

    @property
    def area(self):
        """–í—ã—á–∏—Å–ª—è–µ–º –ø–ª–æ—â–∞–¥—å –ø—Ä–∏ –∫–∞–∂–¥–æ–º –æ–±—Ä–∞—â–µ–Ω–∏–∏."""
        return self._width * self._height

# –ò—Å–ø–æ–ª—å–∑—É–µ–º
rect = Rectangle(10, 5)
print(rect.area)  # 50 (–±–µ–∑ —Å–∫–æ–±–æ–∫!)

