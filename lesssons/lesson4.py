class BankAccount:
    bank_name = "KyrgyzBank"

    def __init__(self, owner, balance=0):
        self.owner = owner
        self.balance = balance

    @classmethod
    def change_bank_name(cls, new_name):
        """–ú–µ–Ω—è–µ–º –∏–º—è –±–∞–Ω–∫–∞ –¥–ª—è –≤—Å–µ—Ö —Å—á–µ—Ç–æ–≤ —Å—Ä–∞–∑—É."""
        cls.bank_name = new_name
        print(f"–ë–∞–Ω–∫ –ø–µ—Ä–µ–∏–º–µ–Ω–æ–≤–∞–Ω –≤ {cls.bank_name}")

# –ò—Å–ø–æ–ª—å–∑—É–µ–º
BankAccount.change_bank_name("OpenAI Bank")

acc1 = BankAccount("–ê–ª–∏–Ω–∞")
acc2 = BankAccount("–ë–µ–∫–∑–∞—Ç")

print(acc1.bank_name)  # OpenAI Bank
print(acc2.bank_name)  # OpenAI Bank

