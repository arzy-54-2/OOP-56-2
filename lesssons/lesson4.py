    def __init__(self, name):
        self._name = name

    @property
    def name(self):
        """–ì–µ—Ç—Ç–µ—Ä"""
        return self._name

    @name.setter
    def name(self, value):
        """–°–µ—Ç—Ç–µ—Ä"""
        if not value.isalpha():
            raise ValueError("–ò–º—è –¥–æ–ª–∂–Ω–æ —Å–æ–¥–µ—Ä–∂–∞—Ç—å —Ç–æ–ª—å–∫–æ –±—É–∫–≤—ã!")
        self._name = value

    @name.deleter
    def name(self):
        """–î–µ–ª–∏—Ç–µ—Ä"""
        print("–£–¥–∞–ª—è–µ–º –∏–º—è!")
        del self._name

