class BankAccount:
    def __init__(self, owner, balance=0):
        self.owner = owner
        self.balance = balance

    def deposit(self, amount):
        """–ú–µ—Ç–æ–¥ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ ‚Äî —Ä–∞–±–æ—Ç–∞–µ—Ç —Å –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–º –æ–±—ä–µ–∫—Ç–æ–º."""
        self.balance += amount
        print(f"{self.owner} –≤–Ω–µ—Å {amount}. –ë–∞–ª–∞–Ω—Å: {self.balance}")

